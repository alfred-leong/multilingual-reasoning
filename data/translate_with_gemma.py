# CLI
# ---------------------------------------------------------------------------


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Translate English responses to native language "
                    "using TranslateGemma for the Translate-DPO experiment."
    )
    parser.add_argument(
        "--language", type=str, default=None,
        help="Language code (e.g. JA_JP). If omitted, run all.",
    )
    parser.add_argument(
        "--config", type=str,
        default=str(PROJECT_ROOT / "configs" / "translate_dpo_config.yaml"),
    )
    group = parser.add_mutually_exclusive_group()
    group.add_argument(
        "--gpu", type=int, default=None,
        help="CUDA device ID for direct GPU inference (recommended). "
             "Loads TranslateGemma in-process — no vLLM server needed.",
    )
    group.add_argument(
        "--port", type=int, default=None,
        help="vLLM server port for TranslateGemma (legacy mode).",
    )
    parser.add_argument(
        "--n_workers", type=int, default=4,
        help="Number of concurrent translation threads (vLLM mode only).",
    )
    parser.add_argument(
        "--max_samples", type=int, default=None,
        help="Max records to translate (for debugging).",
    )
    parser.add_argument(
        "--shard_id", type=int, default=None,
        help="Shard index (0-based). When set, only processes the "
             "corresponding 1/num_shards slice of the dataset.",
    )
    parser.add_argument(
        "--num_shards", type=int, default=1,
        help="Total number of shards to split the dataset into.",
    )
    parser.add_argument(
        "--merge_shards", action="store_true",
        help="Merge previously written shard files into the final output. "
             "Requires --language and --num_shards.",
    )
    args = parser.parse_args()

    config = _load_config(args.config)
    input_dir = PROJECT_ROOT / "data" / "translate_dpo" / "raw"
    output_dir = PROJECT_ROOT / "data" / "translate_dpo" / "translated"

    # --- Merge mode ---------------------------------------------------------
    if args.merge_shards:
        languages = (
            [args.language] if args.language
            else [l["code"] for l in config["languages"]]
        )
        for lang in languages:
            merge_shards(lang, output_dir, args.num_shards)
        return

    # --- Translate mode -----------------------------------------------------
    # Resolve inference mode
    gpu = args.gpu
    port = args.port
    if gpu is None and port is None:
        # Default to vLLM port from config if neither is specified
        port = config["vllm_ports"]["translation"]

    languages = (
        [args.language] if args.language
        else [l["code"] for l in config["languages"]]
    )
    for lang in languages:
        translate_for_language(
            lang, config, input_dir, output_dir,
            port=port, gpu=gpu,
            max_samples=args.max_samples,
            n_workers=args.n_workers,
            shard_id=args.shard_id,
            num_shards=args.num_shards,
        )


if __name__ == "__main__":
    main()
