def merge_shards(
    language_code: str,
    output_dir: Path,
    num_shards: int,
) -> None:
    """Merge shard output files into a single ``{LANG}_translated.jsonl``."""
    merged: list[dict] = []
    for shard_id in range(num_shards):
        shard_path = output_dir / f"{language_code}_translated_shard{shard_id}.jsonl"
        if not shard_path.exists():
            raise FileNotFoundError(
                f"Shard file not found: {shard_path}. "
                f"Make sure all {num_shards} shards completed successfully."
            )
        merged.extend(_load_jsonl(shard_path))

    out_path = output_dir / f"{language_code}_translated.jsonl"
    with open(out_path, "w", encoding="utf-8") as fout:
        for rec in merged:
            fout.write(json.dumps(rec, ensure_ascii=False) + "\n")

    print(f"[{language_code}] Merged {num_shards} shards → {out_path} ({len(merged)} records)")

    # Clean up shard files
    for shard_id in range(num_shards):
        shard_path = output_dir / f"{language_code}_translated_shard{shard_id}.jsonl"
        shard_path.unlink(missing_ok=True)


def translate_for_language(
    language_code: str,
    config: dict,
    input_dir: Path,
    output_dir: Path,
    port: Optional[int] = None,
    gpu: Optional[int] = None,
    max_samples: int | None = None,
    n_workers: int = 4,
    shard_id: Optional[int] = None,
    num_shards: int = 1,
) -> None:
    """Translate all English responses for one language.

    Either *gpu* (direct inference) or *port* (vLLM server) must be given.
    When *gpu* is set, the model is loaded in-process on the specified CUDA
    device and records are translated sequentially.  When *port* is set,
    translation is done via a running vLLM server with concurrent workers.

    When *shard_id* is not None, only processes a 1/*num_shards* slice of
    the dataset and writes to a shard-specific output file.
    """
    shard_suffix = f"_shard{shard_id}" if shard_id is not None else ""
    log_dir = PROJECT_ROOT / "outputs-translate_dpo" / "logs"
    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    logger = _setup_logger(
        name=f"translate.{language_code}{shard_suffix}",
        log_dir=log_dir,
        filename=f"translate_{language_code}{shard_suffix}_{ts}.log",
    )

    # Map project language code (e.g. JA_JP) to ISO code (e.g. ja)
    if language_code not in _TRANSLATE_LANG_CODE:
        raise ValueError(
            f"Unsupported language code: {language_code!r}. "
            f"Supported: {list(_TRANSLATE_LANG_CODE)}"
        )
    target_lang_code = _TRANSLATE_LANG_CODE[language_code]

    prompts = get_translate_dpo_prompts(language_code)
    target_language = prompts["language_name"]

    # Load raw responses
    input_path = input_dir / f"{language_code}_responses.jsonl"
    if not input_path.exists():
        raise FileNotFoundError(
            f"Raw responses not found: {input_path}. "
            "Run generate_translate_dpo_responses.py first."
        )
    records = _load_jsonl(input_path)
    if max_samples is not None:
        records = records[:max_samples]

    # --- Shard slicing ------------------------------------------------------
    total_records = len(records)
    if shard_id is not None:
        shard_size = (total_records + num_shards - 1) // num_shards  # ceil div
        start = shard_id * shard_size
        end = min(start + shard_size, total_records)
        records = records[start:end]
        logger.info(
            "Shard %d/%d: records [%d, %d) of %d",
            shard_id, num_shards, start, end, total_records,
        )

    logger.info("Loaded %d records from %s", len(records), input_path)

    # Translation config
    trans_cfg = config.get("translation", {})
    max_tokens = trans_cfg.get("max_tokens", 2048)
    temperature = trans_cfg.get("temperature", 0.3)
    top_p = trans_cfg.get("top_p", 0.95)

    # --- Prepare output -----------------------------------------------------
    output_dir.mkdir(parents=True, exist_ok=True)
    out_fname = f"{language_code}_translated{shard_suffix}.jsonl"
    out_path = output_dir / out_fname
    translated_records: list[dict] = [None] * len(records)  # preserve order

    # ========================================================================
    # Direct GPU inference path
    # ========================================================================
    if gpu is not None:
        model_id = config["models"]["translation_model"]
        logger.info(
            "Direct GPU inference: model=%s, GPU=%d, lang=%s (%s)",
            model_id, gpu, target_language, target_lang_code,
        )

        translator = DirectTranslator(model_id, gpu_id=gpu)
        logger.info("Model loaded on cuda:%d", gpu)

        for i, record in enumerate(
            tqdm(records, desc=f"[{language_code}] translate (GPU {gpu})", unit="ex")
        ):
            translated_response = translator.translate_response(
                record["english_response"],
                target_lang_code,
                max_new_tokens=max_tokens,
                temperature=temperature,
                top_p=top_p,
            )
            rec = dict(record)
            rec["translated_response"] = translated_response
            translated_records[i] = rec

            logger.debug("idx=%d | translated=%s", i, translated_response[:200])

            if i % 10 == 0:
                logger.info(
                    "\n========== SAMPLE idx=%d ==========\n"
                    "[ENGLISH RESPONSE]\n%s\n\n"
                    "[TRANSLATED RESPONSE]\n%s\n"
                    "====================================",
                    i, record["english_response"], translated_response,
                )

    # ========================================================================
    # vLLM server path (legacy)
    # ========================================================================
    elif port is not None:
        if openai is None:
            raise ImportError(
                "openai package is required for vLLM mode. "
                "Install it with: pip install openai"
            )

        client = openai.OpenAI(
            base_url=f"http://localhost:{port}/v1", api_key="EMPTY"
        )
        available = client.models.list()
        model_name = (
            available.data[0].id
            if available.data
            else config["models"]["translation_model"]
        )

        logger.info("TranslateGemma server: http://localhost:%d/v1", port)
        logger.info("Model: %s", model_name)
        logger.info("Target language: %s (%s)", target_language, target_lang_code)

        def _translate_one(idx: int, record: dict) -> Tuple[int, str]:
            translated = translate_response(
                client, model_name, record["english_response"],
                target_lang_code,
                max_tokens, temperature, top_p,
            )
            return idx, translated

        tqdm_desc = f"[{language_code}{shard_suffix}] translate (port {port})"
        with ThreadPoolExecutor(max_workers=n_workers) as pool:
            futures = {
                pool.submit(_translate_one, i, r): i
                for i, r in enumerate(records)
            }
            for fut in tqdm(
                as_completed(futures),
                total=len(futures),
                desc=tqdm_desc,
                unit="ex",
            ):
                idx, translated_response = fut.result()
                rec = dict(records[idx])
                rec["translated_response"] = translated_response
                translated_records[idx] = rec

                logger.debug(
                    "idx=%d | translated=%s", idx, translated_response[:200]
                )
                if idx % 10 == 0:
                    logger.info(
                        "\n========== SAMPLE idx=%d ==========\n"
                        "[ENGLISH RESPONSE]\n%s\n\n"
                        "[TRANSLATED RESPONSE]\n%s\n"
                        "====================================",
                        idx, records[idx]["english_response"],
                        translated_response,
                    )
    else:
        raise ValueError("Either --gpu or --port must be specified.")

    # --- Write output -------------------------------------------------------
    with open(out_path, "w", encoding="utf-8") as fout:
        for rec in translated_records:
            fout.write(json.dumps(rec, ensure_ascii=False) + "\n")

    logger.info("Translation complete: %d records → %s", len(records), out_path)
    print(f"[{language_code}] {len(records)} translated → {out_path}")


