#!/usr/bin/env python3
"""
Translate English responses to native language using TranslateGemma.

Reads the Setting-1 (English) responses generated by
``generate_translate_dpo_responses.py``, translates the thinking trace and
the final answer separately (preserving ``<think>…</think>`` structure),
and writes the translated response alongside the original data.

Output is written to ``data/translate_dpo/translated/{LANG}_translated.jsonl``.

Usage::

    # Direct GPU inference (recommended — no vLLM server needed):
    python data/translate_with_gemma.py --language JA_JP --gpu 1
    python data/translate_with_gemma.py --gpu 1             # all languages

    # Legacy vLLM mode:
    python data/translate_with_gemma.py --language JA_JP --port 8401
    python data/translate_with_gemma.py --language JA_JP --max_samples 50
"""

import argparse
import json
import logging
import re
import sys
from concurrent.futures import ThreadPoolExecutor, as_completed
from datetime import datetime
from pathlib import Path
from typing import Optional, Tuple

import yaml
from tqdm import tqdm

try:
    import openai
except ImportError:
    openai = None  # only needed for vLLM mode

PROJECT_ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(PROJECT_ROOT))

from prompts.translate_dpo_prompts import get_translate_dpo_prompts

# ---------------------------------------------------------------------------
# Logging
